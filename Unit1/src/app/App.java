package app;


public class App {
    public static void main(String[] args) throws Exception {

        for (int i = 10; i <= 20; i = i + 1){
            System.out.println("the count is " + i);
            System.out.println("Lets keep counting");
        }

        for (int i2 = 10; i2 <= 100; i2 = i2 + 1){
            System.out.println("the count is " + i2);
            System.out.println("lets keep counting");
        }

        for (int i3 = 1000; i3 <= 256000; i3 = i3 + 1){
            System.out.println("the count is " + i3);
            System.out.println("lets keep counting");
        }

        for (int i4 = 11; i4 <= 12; i4 = i4 + 1){
            System.out.println("the count is " + i4);
            System.out.println("lets keep counting");
        }

        for (int i5 = 10; i5 >= 0; i5 = i5 - 1){
            System.out.println("the count is " + i5);
            System.out.println("lets keep subtracting");
        }

    } 
}